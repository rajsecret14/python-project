# 1. list method sort()
l1 = [1,8,9,5,750,80,900,400] # l1 got changed directly in case of list
print(l1)
l1.sort()
print(l1)
# reverse the list 
l1.reverse() #original list will be reversed 
print(l1)
# add anything at the end of the list
l1.append(False)
print(l1)
# it can insert anywhere
l1.insert(2,838)
l1.insert(-4,200000)
print(l1)
# pop- it can delete anything from the list from index 2
l1.pop(2)
print(l1)
# remove 400 from list
l1.remove(400)
print(l1)



